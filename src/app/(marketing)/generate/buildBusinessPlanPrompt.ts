// app/generate/buildBusinessPlanPrompt.ts
import { WizardAnswers } from "./wizardSchema";

interface ContextParams {
    industry: string; // з твого стартового кроку
    country: string;  // з твого стартового кроку
}

export function buildBusinessPlanPrompt(
    answers: WizardAnswers,
    ctx: ContextParams
): string {
    return `
Ти — помічник-проектувальник бізнес-планів українською мовою.

На основі даних нижче створи структурований бізнес-план з такими розділами:
1. Короткий опис проєкту
2. Опис продукту / послуги
3. Цільова аудиторія та проблема клієнта
4. Конкурентні переваги
5. Формат роботи та локація
6. Команда та організаційна структура
7. Ціноутворення та очікувана виручка
8. Основні витрати
9. Інвестиційні потреби (якщо вказано)
10. План розвитку на 6–12 місяців
11. Висновок

Пиши простою, зрозумілою мовою, без канцеляризмів, але достатньо формально, щоб документ можна було подати в банк, на грант або інвестору.

Дані про проєкт:

- Сфера бізнесу: ${ctx.industry || "не вказано"}
- Країна/місто: ${ctx.country || "не вказано"}

- Що продає бізнес: ${answers.product || "не вказано"}
- Формат роботи: ${answers.workFormat || "не вказано"}
- Хто клієнти: ${answers.targetCustomer || "не вказано"}
- Яка проблема/потреба: ${answers.problem || "не вказано"}
- Унікальність / переваги: ${answers.uniqueness || "не вказано"}

- Команда: ${answers.team || "не вказано"}
- Ціни / середній чек: ${answers.pricing || "не вказано"}
- Основні щомісячні витрати: ${answers.monthlyCosts || "не вказано"}
- Очікувана виручка на місяць: ${answers.expectedRevenue || "не вказано"}

- Мета бізнес-плану: ${answers.planPurpose || "не вказано"}
- Необхідні стартові інвестиції: ${answers.startupInvestment || "не вказано"}
- План розвитку на 6–12 місяців: ${answers.growthPlan || "не вказано"}

Створи послідовний, логічний бізнес-план, розбий на підрозділи з підзаголовками, використовуй марковані списки там, де це доречно.
`.trim();
}
